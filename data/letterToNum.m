function num = letterToNum(l)
% single letter to a number.
% a = 1, z = 26. base 26.

if l == 'A'
	num = 1;
elseif l == 'B'
	num = 2;
elseif l == 'C'
	num = 3;
elseif l == 'D'
	num = 4;
elseif l == 'E'
	num = 5;
elseif l == 'F'
	num = 6;
elseif l == 'G'
	num = 7;
elseif l == 'H'
	num = 8;
elseif l == 'I'
	num = 9;
elseif l == 'J'
	num = 10;
elseif l == 'K'
	num = 11;
elseif l == 'L'
	num = 12;
elseif l == 'M'
	num = 13;
elseif l == 'N'
	num = 14;
elseif l == 'O'
	num = 15;
elseif l == 'P'
	num = 16;
elseif l == 'Q'
	num = 17;
elseif l == 'R'
	num = 18;
elseif l == 'S'
	num = 19;
elseif l == 'T'
	num = 20;
elseif l == 'U'
	num = 21;
elseif l == 'V'
	num = 22;
elseif l == 'W'
	num = 23;
elseif l == 'X'
	num = 24;
elseif l == 'Y'
	num = 25;
elseif l == 'Z'
	num = 26;
end
